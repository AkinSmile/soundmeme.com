function playMusic() {
    const audio = document.getElementById("myAudio");
  
    audio.volume = 1.0; // Устанавливаем громкость на максимум
  
    audio.play()
      .then(() => {
        // Аудио успешно воспроизводится
      })
      .catch(error => {
        // Обрабатываем ошибку (например, если браузер блокирует автовоспроизведение)
        console.error("Ошибка воспроизведения:", error);
        alert("Для воспроизведения звука нужно взаимодействие с пользователем (возможно, нужно нажать еще раз).");
      });
  }
  
  